new_dict = {"Оксана Забужко": "Польові дослідження з українського сексу", "Юрий Андрухович": "Перверзія",
            "Сергей Жадан": "Месопотамія", "Любко Дереш": "Культ"}
score = 0
for k, v in new_dict.items():
    answer = input(f"Кто написал '{v}'?")
    if answer == k:
        score += 1
print(f"Тест окончен! Количество правильных ответов: {score}")
